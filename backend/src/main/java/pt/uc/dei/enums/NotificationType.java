package pt.uc.dei.enums;

/**
 * Enum representing different types of notifications.
 * <ul>
 *   <li>MESSAGE: Notification related to user messages.</li>
 *   <li>EVALUATION: Notification about evaluations.</li>
 *   <li>COURSE: Notification regarding course updates or events.</li>
 * </ul>
 */
public enum NotificationType {
    MESSAGE,
    APPRAISAL,
    CYCLE,
    COURSE;
}